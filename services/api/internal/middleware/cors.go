package middleware

import (
	"net/http"
	"strings"
)

// CORS returns middleware that sets permissive CORS headers for the
// given list of allowed origins.  Preflight OPTIONS requests are
// short-circuited with a 204 response.
func CORS(origins []string) func(http.Handler) http.Handler {
	allowSet := make(map[string]struct{}, len(origins))
	for _, o := range origins {
		allowSet[strings.TrimRight(o, "/")] = struct{}{}
	}

	return func(next http.Handler) http.Handler {
		return http.HandlerFunc(func(w http.ResponseWriter, r *http.Request) {
			origin := r.Header.Get("Origin")
			if origin == "" {
				next.ServeHTTP(w, r)
				return
			}

			allowed := false
			if _, ok := allowSet["*"]; ok {
				allowed = true
			} else if _, ok := allowSet[origin]; ok {
				allowed = true
			}

			if !allowed {
				next.ServeHTTP(w, r)
				return
			}

			w.Header().Set("Access-Control-Allow-Origin", origin)
			w.Header().Set("Access-Control-Allow-Methods", "GET, POST, PUT, PATCH, DELETE, OPTIONS")
			w.Header().Set("Access-Control-Allow-Headers", "Authorization, Content-Type, X-Request-ID")
			w.Header().Set("Access-Control-Allow-Credentials", "true")
			w.Header().Set("Access-Control-Max-Age", "86400")

			if r.Method == http.MethodOptions {
				w.WriteHeader(http.StatusNoContent)
				return
			}

			next.ServeHTTP(w, r)
		})
	}
}
